    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int position = tableMain.getSelectedRow();// lay vi tri row minh chon de xoa
        String data = tableMain.getModel().getValueAt(position, 0).toString();
        // getModel nội dung // ValueAt: bạn muốn lấy dòng nào, cột nào
        // cột "0" hiện là maNV
        int result = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắc muốn xóa không ?");
        if (result == 0) {
            try {
                java.sql.Connection connection = DBConnection.getConnection();
                String sql = "DELETE FROM dbo.[NhaKH] WHERE  IdNhaKH= ?";

                PreparedStatement ps = connection.prepareStatement(sql);
                ps.setString(1, data);

                ps.executeUpdate();
                showDuLieu();
                DBConnection.closeConnection(connection);
                JOptionPane.showMessageDialog(null, "Xóa thành công ");
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

