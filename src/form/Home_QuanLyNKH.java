    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            tableMain.removeAll();
            String[] arr = {"ID nhà khoa học", "Tên nhà khoa học", "Quê Quán", "Chuyên ngành"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);

            java.sql.Connection connection = DBConnection.getConnection();

            String sql = "SELECT * FROM NhaKH where Hoten like ?";

            //statement.setString(1, "%"+fullname+"%");

            PreparedStatement ps = connection.prepareStatement(sql);
            ps.setString(1,txtfind.getText());
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                 Vector vector = new Vector();
                vector.add(rs.getString("IdNhaKH"));
                vector.add(rs.getString("Hoten"));
                vector.add(rs.getString("QueQuan"));
                vector.add(rs.getString("ChuyenNganh"));
                model.addRow(vector);
            }
            tableMain.setModel(model);

        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

